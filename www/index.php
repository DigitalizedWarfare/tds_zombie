	  					<img src="images/ID-100167764.jpg" width="277" height="266" alt="" />
	  					<h3>What is the Zombie Control Panel?</h3>
	  					<br class="clear bottomClear" />
	  					<p>The Zombie Comtrol Panel makes it easy for you to install and configure the honeypot systems we support. This should help newer people get honeypots deployed quicker and with more ease. By using the tabs at the top of the screen, you will be able to configure, as well as control which honeypots are currently running.</p>
						<br class="clear bottomClear" />
						<h4>What are Honey Pots?</h4>
	  					<p>Honey Pot Systems are decoy servers or systems setup to gather information regarding an attacker or intruder into your system. It is important to remember that Honey Pots do not replace other traditional Internet security systems; they are an additional level or system.</p>
						<br class="clear bottomClear" />
						<h4>How can Honeypots be Deployed?</h4>
						<p>Honey Pots can be setup inside, outside or in the DMZ of a firewall design or even in all of the locations although they are most often deployed inside of a firewall for control purposes. In a sense, they are variants of standard Intruder Detection Systems (IDS) but with more of a focus on information gathering and deception. <br /><br />
There are many variations of Honey pots. See <a href="http://en.wikipedia.org/wiki/Honeypot_%28computing%29">Wiki</a> for more information.</p>
      						<p class="quoteBar">[A combination of traditional network security monitoring and recent advancements in honeypot and active defense tools is key to detecting today's threats. See Tech Insight: <a href="http://www.darkreading.com/vulnerability/tech-insight-time-to-set-up-that-honeypo/240153736?itc=edit_in_body_cross">Time To Set Up That Honeypot</a>.]</p>
	  					<p>The Honeypots we have put togethrer for you are what we consider the most common honeypots availible. Each one is loaded from its GITHUB repo to ensure the most up to date version that we can get, when we can get it. Each version will be installed into /opt to ensire it does not conflict with your files system. If you need to update, please just got to your desired folder and run : git pull</p>
